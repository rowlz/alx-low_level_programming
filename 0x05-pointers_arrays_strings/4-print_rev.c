/**
 * print_rev - prints a string in reverse
 * @s: string
 * Return: Always 0
*/

void print_rev(char *s)
{
	int a;
	int b;

	for (a = 0; s[a] != '\0'; ++a)
	{

	}

	b = a;

	for (; s[b] <= b; --b)
	{
		printf("%c",s[b]);
	}

	printf("\n");
}
